import pandas as pd
import geopandas as gpd
from pathlib import Path

listings = pd.read_csv("data/processed_data/listings.csv")

zips = gpd.read_file(
    "data/spatial/tl_2020_us_zcta520.shp"
)

print(zips.shape)
