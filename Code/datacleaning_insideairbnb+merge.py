listings_gdf = gpd.GeoDataFrame(
    listings.copy(),
    geometry=gpd.points_from_xy(listings["longitude"], listings["latitude"]),
    crs="EPSG:4326"
)

zips = zips.to_crs("EPSG:4326")

joined = gpd.sjoin(
    listings_gdf,
    zips[["ZCTA5CE20", "geometry"]],
    how="left",
    predicate="within"
).rename(columns={"ZCTA5CE20": "zip"})

joined["zip"] = (
    joined["zip"]
    .astype("string")
    .str.extract(r"(\d+)", expand=False)
    .str.zfill(5)
)

mapped = joined.dropna(subset=["zip"]).drop(columns=["index_right", "geometry"])

